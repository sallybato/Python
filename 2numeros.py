soma = (num1+num2)
print("A soma é >", soma)

produto = num1 * (num2 * num2)
print("O produto é >",produto)

quadrado = (num1 * num1)
print("O quadrado é >",quadrado)

raiz_quadrada = math.sqrt(num1**2 + num2**2)
print("A raiz é >",raiz_quadrada)

sen = math.sin(num1 - num2)
print("A raiz é >", sen)

